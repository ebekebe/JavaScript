let pokemon = [];

// Push - Adds value to end of an array
pokemon.push('Pokemon');
pokemon.push('MewTwo', 'Strikes', 'Back!');

console.log(pokemon);

// Join - flattens an array to return single string dilineated by argument
console.log(pokemon.join(' '));

// Pop - Removes value from end of an array and returns it
console.log(pokemon.pop());
console.log(pokemon);
